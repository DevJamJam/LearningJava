package exercise;

public class Type1 {
    /**
     * int, double, boolean, char, String 로 변수 선언 해보기
     * int = 내가 좋아하는 숫자
     * double = 원주율
     * boolean은 민트 호,불호
     * Char 내 성 or 이니셜 첫글자
     * String은 내 한국어 닉네임
     */

    public static void main(String[] args){
        int myFavoriteNumber = 25;
        double pi = 3.141592;
        boolean likeMint = true;
        char mySurnameFirstAlphabet = 'J';
        String myNicName = "잼잼";

        System.out.println(myFavoriteNumber);
        System.out.println(pi);
        System.out.println(likeMint);
        System.out.println(mySurnameFirstAlphabet);
        System.out.println(myNicName);
    }
}
